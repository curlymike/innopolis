package part1.lesson07.task1;

import org.junit.Test;

import java.math.BigInteger;
import java.util.Map;
import java.util.concurrent.ExecutorService;
import java.util.concurrent.Executors;
import java.util.concurrent.Future;

import static org.assertj.core.api.Assertions.assertThat;

public class ParallelFactorialCallableTest {

  @Test
  public void test() throws Exception {
    Map.Entry<Integer, BigInteger> pair = new ParallelFactorialCallable(4).call();
    assertThat(pair.getValue()).isEqualTo(BigInteger.valueOf(24));
  }

  @Test
  public void compute001() throws Exception {
    ExecutorService es = Executors.newFixedThreadPool(2);
    Future<Map.Entry<Integer, BigInteger>> f = es.submit(new ParallelFactorialCallable(4));
    assertThat(f.get().getValue()).isEqualTo(BigInteger.valueOf(24));
  }

  @Test
  public void compute002() throws Exception {
    ExecutorService es = Executors.newFixedThreadPool(2);
    Future<Map.Entry<Integer, BigInteger>> f = es.submit(new ParallelFactorialCallable(5));
    assertThat(f.get().getValue()).isEqualTo(BigInteger.valueOf(120));
  }

  @Test
  public void compute003() throws Exception {
    ExecutorService es = Executors.newFixedThreadPool(2);
    Future<Map.Entry<Integer, BigInteger>> f = es.submit(new ParallelFactorialCallable(6));
    assertThat(f.get().getValue()).isEqualTo(BigInteger.valueOf(720));
  }

  @Test
  public void compute004() throws Exception {
    ExecutorService es = Executors.newFixedThreadPool(2);
    Future<Map.Entry<Integer, BigInteger>> f = es.submit(new ParallelFactorialCallable(7));
    assertThat(f.get().getValue()).isEqualTo(BigInteger.valueOf(5040));
  }

  @Test
  public void compute005() throws Exception {
    ExecutorService es = Executors.newFixedThreadPool(2);
    Future<Map.Entry<Integer, BigInteger>> f = es.submit(new ParallelFactorialCallable(8));
    assertThat(f.get().getValue()).isEqualTo(BigInteger.valueOf(40320));
  }

  @Test
  public void compute006() throws Exception {
    ExecutorService es = Executors.newFixedThreadPool(2);
    Future<Map.Entry<Integer, BigInteger>> f = es.submit(new ParallelFactorialCallable(16));
    assertThat(f.get().getValue()).isEqualTo(BigInteger.valueOf(20922789888000L));
  }

}